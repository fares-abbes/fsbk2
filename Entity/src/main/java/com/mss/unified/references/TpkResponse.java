package com.mss.unified.references;

public class TpkResponse {
	
	private String keyLmk;
	private String keyIso;
	private String kcv;
	
	public String getKeyLmk() {
		return keyLmk;
	}
	public void setKeyLmk(String keyLmk) {
		this.keyLmk = keyLmk;
	}
	public String getKeyIso() {
		return keyIso;
	}
	public void setKeyIso(String keyIso) {
		this.keyIso = keyIso;
	}
	public String getKcv() {
		return kcv;
	}
	public void setKcv(String kcv) {
		this.kcv = kcv;
	}
	

}
